    /**
     * bitmap转字节数组
     *
     * @param bitmap
     * @return 字节数组
     */
    public static byte[] bitmap2Byte(Bitmap bitmap) {
        ByteArrayOutputStream bos = new ByteArrayOutputStream();
        bitmap.compress(Bitmap.CompressFormat.PNG, 100, bos);//将图片以PNG的格式指定的字节流
        return bos.toByteArray();
    }

    /**
     * 字节数组转bitmap
     *
     * @param bytes
     * @return bitmap
     */
    public static Bitmap byte2Bitmap(byte[] bytes) {
        return (bytes == null || bytes.length == 0) ? null : BitmapFactory.decodeByteArray(bytes,
                0, bytes.length);
    }

